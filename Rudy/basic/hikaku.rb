# 比較演算子
# 結果は真(true)か偽(false)
result1 = 1 < 2    # true
puts result1

result2 = 1 == 2   # false
puts result2



# ==	a == b	aとbが等しい場合にはtrueを返す。等しくない場合にはfalseを返す。
# !=	a != b	aとbが等しくない場合にはtrueを返す。等しい場合にはfalseを返す。
# <	  a < b	  aよりbが大きい場合にはtrueを返す。aよりbが小さいか等しい場合にはfalseを返す。
# >	  a > b	  aよりbが小さい場合にはtrueを返す。aよりbが大きいか等しい場合にはfalseを返す。
# <=	a <= b	aよりbが大きいか等しい場合にはtrueを返す。
# >=	a >= b	aよりbが小さいか等しい場合にはtrueを返す。